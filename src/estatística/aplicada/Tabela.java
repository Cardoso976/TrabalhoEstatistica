package estatística.aplicada;

public class Tabela {
	private int Li;
	private int Ls;
	private int Fi;
	private int FiTotal;
	private int Fac;
	private float Xi;
	private float XiFi;
	private float variancia;

	public float getVariancia() {
		return variancia;
	}

	public void setVariancia(float variancia) {
		this.variancia = variancia;
	}

	public float getXifi() {
		return XiFi;
	}

	public void setXifi(float xiFi) {
		XiFi = xiFi;
	}

	public int getFiTotal() {
		return FiTotal;
	}

	public void setFiTotal(int fiTotal) {
		FiTotal = fiTotal;
	}

	public int getFac() {
		return Fac;
	}

	public void setFac(int fac) {
		Fac = fac;
	}

	public float getXi() {
		return Xi;
	}

	public void setXi(float xi) {
		Xi = xi;
	}

	public int getFi() {
		return Fi;
	}

	public void setFi(int fi) {
		Fi = fi;
	}

	public int getLi() {
		return Li;
	}

	public void setLi(int li) {
		Li = li;
	}

	public int getLs() {
		return Ls;
	}

	public void setLs(int ls) {
		Ls = ls;
	}

}
